/*--------------------Déclarations--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
#include <stdio.h>
#define TAILLE_TABLEAU 10

int *creer_tableau(void);
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/



/*----------Code-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
int main (void)
{
	int *tab = creer_tableau();

	for(int i = 0; i < TAILLE_TABLEAU; i++)
	{
	printf("[%d]", tab[i]);
	}

}
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/




/*--------------------Création Tableau 1 dimension--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
int *creer_tableau(void)
{

	static int tabentier [TAILLE_TABLEAU] = {5,6,7,8,9,6,9,4,5,7}; //le static permet d'éviter le soucis de variable local
	int i;

	for(i = 0; i < 5; i++)
		{
			tabentier[i];
			return tabentier;
		}
}
